// a number and checks whether it is even or odd.

var n=parseInt(prompt("Enter Any Number : "));
if(n%2==0){
    console.log("Given Number Is even Number.")
}
else if (n===0){
    console.log("Given Number Is 'Zero'. ")
}
else{
    console.log("Given Number Is odd Number. ")
}